package com.xworkz.oops.encapsulation;

public class flower {
	private String name;
	private String stamen;
	private String anther;
	private String filament;
	private String pistill;
	private String stigma;
	private String style;
	private String ovary;
	private String ovul;
	private String petal;
	
	
	public flower()
	{
		
	}
	
	public String getName()
	{
		return name;
	}
	public void setName(String nam)
	{
		name=nam;
	}
	
	public String getStamen()
	{
		return stamen;
	}
	public void setStemen(String stem )
	{
		stamen=stem;
	}
	
	public String getAnther()
	{
		return anther;
	}
	
	public void setAnther(String anthe)
	{
		anther=anthe;
	}
	
	public String getFilament()
	{
		return filament;
	}
	
	public void setFilament(String filame)
	{
		filament=filame;
	}
	
	public String getPistill()
	{
		return pistill;
	}
	
	public void setPistill(String pistil)
	{
		pistill=pistil;
	}
	
	public String getStigma()
	{
		return stigma;
	}
	
	public void setStigma(String stigm)
	{
		stigma=stigm;
	}
	
	public String getStyle()
	{
		return style;
	}
	
	public void setStyle(String styl)
	{
		style=styl;
	}
	
	public String getOvary()
	{
		return ovary;
	}
	
	public void setOvary(String ovar)
	{
		ovary=ovar;
	}
	
	public String getOvul()
	{
		return ovul;
	}
	
	public void setOvul(String ovu)
	{
		ovul=ovu;
	}
	
	public String getPetal()
	{
		return petal;
	}
	
	public void setPetal(String peta)
	{
		petal=peta;
	}
}































